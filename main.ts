function play_note(freq: number, length: number, velocity: number, key_number: number, key_type: string) {
    let from_color = 15
    let to_color = 8
    let key_image = image.create(3, Math.max(Math.round(length / 20) - 2, 1))
    key_image.fill(to_color)
    let key_sprite = sprites.create(key_image, SpriteKind.Food)
    let current_left = 0
    for (let index = 0; index < key_number; index++) {
        let key_type = octave[(9 + index) % 12]
        let next_key_type = octave[(index + 10) % 12]
        if (key_type == " ") {
            if (next_key_type == " ") {
                current_left += 4
            } else {
                current_left += 3
            }
        } else {
            current_left += 1
        }
    }
    key_sprite.left = current_left
    key_sprite.bottom = 0
    key_sprite.vy = 50
    sprites.setDataNumber(key_sprite, "key_number", key_number)
    sprites.setDataNumber(key_sprite, "frequency", freq)
    sprites.setDataNumber(key_sprite, "length", length)
    sprites.setDataNumber(key_sprite, "velocity", velocity)
    if (key_type == " ") {
        from_color = 1
        to_color = 9
        key_sprite.image.fill(to_color)
    } else {
        from_color = 15
        to_color = 8
        key_sprite.left -= 1
    }
    sprites.setDataNumber(key_sprite, "from_color", from_color)
    sprites.setDataNumber(key_sprite, "to_color", to_color)
    pause(length)
    key_sprite.setFlag(SpriteFlag.AutoDestroy, true)
}
